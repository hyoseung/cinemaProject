package sk.movie.ticketInfo;

public class ReservTicketVO {
	String id;
	String movieName;
	String theater;
	String viewingDate;
	String viewingTime;
	String viewingCode;
	String seat;
	String person;
	String reserveDate;
	String reserveCode;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMovieName() {
		return movieName;
	}
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	public String getTheater() {
		return theater;
	}
	public void setTheater(String theater) {
		this.theater = theater;
	}
	public String getViewingDate() {
		return viewingDate;
	}
	public void setViewingDate(String viewingDate) {
		this.viewingDate = viewingDate;
	}
	public String getViewingTime() {
		return viewingTime;
	}
	public void setViewingTime(String viewingTime) {
		this.viewingTime = viewingTime;
	}
	public String getViewingCode() {
		return viewingCode;
	}
	public void setViewingCode(String viewingCode) {
		this.viewingCode = viewingCode;
	}
	public String getSeat() {
		return seat;
	}
	public void setSeat(String seat) {
		this.seat = seat;
	}
	public String getPerson() {
		return person;
	}
	public void setPerson(String person) {
		this.person = person;
	}
	public String getReserveDate() {
		return reserveDate;
	}
	public void setReserveDate(String reserveDate) {
		this.reserveDate = reserveDate;
	}
	public String getReserveCode() {
		return reserveCode;
	}
	public void setReserveCode(String reserveCode) {
		this.reserveCode = reserveCode;
	}
	
	
	
}
